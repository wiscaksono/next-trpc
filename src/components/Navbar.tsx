const NewNoteModal = ({
  newNote,
  setNewNote,
}: {
  newNote: boolean;
  setNewNote: (value: boolean) => void;
}) => {
  const [data, setData] = useState<{ title: string; content: string }>({
    title: "",
    content: "",
  });
  const [loading, setLoading] = useState<boolean>(false);
  const createNote = api.notes.createNotes.useMutation();

  const handleSubmit = (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    setLoading(true);
    try {
      createNote.mutate({
        title: data.title,
        content: data.content,
      });
      setNewNote(false);
      setLoading(false);
      toast.success("Note created successfully");
    } catch (error: unknown) {
      const errorMessage = (error as Error).toString();
      toast.error(`Something went wrong: ${errorMessage}`);
    }
  };

  return (
    <Transition appear show={newNote} as={Fragment}>
      <Dialog
        as="div"
        className="relative z-10"
        onClose={() => setNewNote(false)}
      >
        <Transition.Child
          as={Fragment}
          enter="ease-out duration-300"
          enterFrom="opacity-0"
          enterTo="opacity-100"
          leave="ease-in duration-200"
          leaveFrom="opacity-100"
          leaveTo="opacity-0"
        >
          <div className="fixed inset-0 bg-black bg-opacity-25" />
        </Transition.Child>

        <div className="fixed inset-0 overflow-y-auto">
          <div className="flex min-h-full items-center justify-center p-4 text-center">
            <Transition.Child
              as={Fragment}
              enter="ease-out duration-300"
              enterFrom="opacity-0 scale-95"
              enterTo="opacity-100 scale-100"
              leave="ease-in duration-200"
              leaveFrom="opacity-100 scale-100"
              leaveTo="opacity-0 scale-95"
            >
              <Dialog.Panel className="w-full max-w-md transform overflow-hidden rounded-2xl bg-white p-6 text-left align-middle shadow-xl transition-all">
                <Dialog.Title
                  as="h3"
                  className="text-lg font-medium leading-6 text-gray-900"
                >
                  Create a new note
                </Dialog.Title>
                <form
                  onSubmit={(e) => void handleSubmit(e)}
                  className="mt-4 space-y-6"
                >
                  <div>
                    <label
                      htmlFor="email"
                      className="mb-2 block text-sm font-medium text-gray-500"
                    >
                      Title
                    </label>
                    <input
                      type="text"
                      name="email"
                      id="email"
                      className="block w-full rounded-lg border border-gray-300 bg-gray-50 p-2.5 text-sm text-gray-900 focus:border-blue-500 focus:ring-blue-500"
                      required
                      onChange={(e) =>
                        setData({ ...data, title: e.target.value })
                      }
                    />
                  </div>
                  <div>
                    <label
                      htmlFor="email"
                      className="mb-2 block text-sm font-medium text-gray-500"
                    >
                      Content
                    </label>
                    <textarea
                      name="content"
                      id="content"
                      cols={30}
                      rows={10}
                      className="block w-full rounded-lg border border-gray-300 bg-gray-50 p-2.5 text-sm text-gray-900 focus:border-blue-500 focus:ring-blue-500"
                      onChange={(e) =>
                        setData({ ...data, content: e.target.value })
                      }
                    />
                  </div>
                  <div className="mt-4">
                    <button
                      type="submit"
                      className="inline-flex justify-center rounded-md border border-transparent bg-blue-100 px-4 py-2 text-sm font-medium text-blue-900 transition-colors hover:bg-blue-200 focus:outline-none focus-visible:ring-2 focus-visible:ring-blue-500 focus-visible:ring-offset-2"
                      disabled={loading}
                    >
                      Create
                    </button>
                    <button
                      type="button"
                      className="inline-flex justify-center rounded-md border border-transparent px-4 py-2 text-sm font-medium text-blue-900 focus:outline-none focus-visible:ring-2 focus-visible:ring-blue-500 focus-visible:ring-offset-2"
                      disabled={loading}
                      onClick={() => void setNewNote(false)}
                    >
                      Cancel
                    </button>
                  </div>
                </form>
              </Dialog.Panel>
            </Transition.Child>
          </div>
        </div>
      </Dialog>
    </Transition>
  );
};
